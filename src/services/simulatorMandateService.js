/**
 * Validate simulated mandate completeness (for learning feedback)
 */
export async function validateSimMandate(mandateId) {
  const mandate = await getSimMandateById(mandateId)
  const deliverables = await getSimDeliverables(mandateId)
  const stakeholders = await getSimStakeholders(mandateId)
  
  const errors = []
  const warnings = []
  
  // Required fields
  if (!mandate.mandate_title) errors.push('Mandate title is required')
  if (!mandate.purpose || mandate.purpose.length < 20) errors.push('Purpose must be at least 20 characters')
  if (!mandate.background || mandate.background.length < 100) errors.push('Background must be at least 100 characters')
  if (!mandate.project_objectives || mandate.project_objectives.length < 100) errors.push('Project objectives must be at least 100 characters')
  if (!mandate.outline_business_case || mandate.outline_business_case.length < 100) errors.push('Outline business case must be at least 100 characters')
  
  // Learning recommendations
  if (deliverables.length === 0) warnings.push('Consider adding at least one deliverable')
  if (stakeholders.length === 0) warnings.push('Consider identifying customers/users')
  if (!mandate.proposed_executive_name && !mandate.proposed_pm_name) warnings.push('Consider identifying proposed Executive or PM')
  
  // Calculate practice score (for learning feedback)
  const totalChecks = 7
  const passedChecks = totalChecks - errors.length
  const practiceScore = Math.round((passedChecks / totalChecks) * 100)
  
  return {
    isValid: errors.length === 0,
    errors,
    warnings,
    practiceScore,
    feedback: errors.length === 0 
      ? 'Great work! Your mandate meets all requirements.' 
      : 'Review the errors above to improve your mandate.'
  }
}

/**
 * Create a practice project from an approved simulated mandate
 * @param {string} mandateId - The ID of the approved mandate
 * @param {string} simulationRunId - Optional simulation run ID for full lifecycle practice
 * @returns {Promise<string>} The ID of the newly created practice project
 */
export async function createPracticeProjectFromMandate(mandateId, simulationRunId = null) {
  const userId = await getCurrentUserId()
  
  // Validate mandate is approved
  const mandate = await getSimMandateById(mandateId)
  if (mandate.document_status !== 'approved') {
    throw new Error('Only approved practice mandates can create practice projects')
  }
  
  const { data, error } = await simDb.rpc('create_practice_project_from_mandate', {
    p_mandate_id: mandateId,
    p_user_id: userId,
    p_simulation_run_id: simulationRunId
  })
  
  if (error) throw error
  return data
}

/**
 * Check if a practice project can be created from a mandate
 * @param {string} mandateId - The ID of the mandate
 * @returns {Promise<boolean>} True if a practice project can be created
 */
export async function canCreatePracticeProject(mandateId) {
  try {
    const mandate = await getSimMandateById(mandateId)
    return (
      mandate.document_status === 'approved' &&
      mandate.is_practice_mode === true
    )
  } catch (error) {
    console.error('Error checking practice project creation eligibility:', error)
    return false
  }
}

/**
 * Get practice projects created from a mandate
 * @param {string} mandateId - The ID of the mandate
 * @returns {Promise<array>} List of practice projects
 */
export async function getPracticeProjectsFromMandate(mandateId) {
  const { data, error } = await simDb
    .from('practice_projects')
    .select('*')
    .eq('mandate_id', mandateId)
    .eq('is_practice_mode', true)
    .order('created_at', { ascending: false })
  
  if (error) throw error
  return data || []
}

/**
 * Calculate learning progress (completion %)
 */
export async function getSimMandateProgress(mandateId) {
  const mandate = await getSimMandateById(mandateId)
  const deliverables = await getSimDeliverables(mandateId)
  const stakeholders = await getSimStakeholders(mandateId)
  
  const sections = {
    purpose: !!mandate.purpose && mandate.purpose.length >= 20,
    authority: !!mandate.authority_responsible,
    background: !!mandate.background && mandate.background.length >= 100,
    objectives: !!mandate.project_objectives && mandate.project_objectives.length >= 100,
    scope: deliverables.length > 0,
    constraints: !!mandate.constraints,
    interfaces: !!mandate.interfaces,
    quality: !!mandate.quality_expectations && !!mandate.quality_priority,
    businessCase: !!mandate.outline_business_case && mandate.outline_business_case.length >= 100,
    roles: !!(mandate.proposed_executive_name || mandate.proposed_pm_name),
    stakeholders: stakeholders.length > 0,
  }
  
  const completed = Object.values(sections).filter(Boolean).length
  const total = Object.keys(sections).length
  
  return {
    percentage: Math.round((completed / total) * 100),
    completed,
    total,
    sections
  }
}
