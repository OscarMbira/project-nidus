/**
 * Simulator Mandate Service
 * Handles simulated project mandate operations for learning/practice
 * 
 * IMPORTANT: Uses simDb (sim schema) - NOT platformDb (public schema)
 * These are for learning only - don't create real projects
 */

import { simDb } from './supabase/supabaseClient'

/**
 * Get current user ID from auth
 */
async function getCurrentUserId() {
  const { data: { user } } = await simDb.auth.getUser()
  if (!user) throw new Error('User not authenticated')
  return user.id
}

/**
 * Create a simulated mandate (for learning/practice)
 */
export async function createSimMandate(mandateData) {
  const userId = await getCurrentUserId()
  
  const insertData = {
    ...mandateData,
    user_id: userId,
    document_status: mandateData.document_status || 'draft',
    version_number: mandateData.version_number || '1.0',
    created_date: mandateData.created_date || new Date().toISOString().split('T')[0],
    is_standalone: mandateData.is_standalone !== undefined ? mandateData.is_standalone : true,
    quality_priority: mandateData.quality_priority || 'balanced',
    is_active: true,
    is_practice_mode: true, // Always true for simulator
  }
  
  // mandate_reference will be auto-generated by trigger if not provided
  
  const { data, error } = await simDb
    .from('project_mandates')
    .insert(insertData)
    .select()
    .single()
  
  if (error) throw error
  return data
}

/**
 * Get simulated mandate by ID
 */
export async function getSimMandateById(mandateId) {
  const { data, error } = await simDb
    .from('project_mandates')
    .select(`
      *,
      simulation_run:simulation_run_id (id, status, scenario_id, started_at)
    `)
    .eq('id', mandateId)
    .eq('is_active', true)
    .single()
  
  if (error) throw error
  return data
}

/**
 * Get all simulated mandates for current user
 */
export async function getAllSimMandates(filters = {}) {
  const userId = await getCurrentUserId()
  
  let query = simDb
    .from('project_mandates')
    .select(`
      *,
      simulation_run:simulation_run_id (id, status, scenario_id)
    `)
    .eq('user_id', userId)
    .eq('is_active', true)
  
  if (filters.status) {
    query = query.eq('document_status', filters.status)
  }
  
  if (filters.simulation_run_id) {
    query = query.eq('simulation_run_id', filters.simulation_run_id)
  }
  
  if (filters.search) {
    query = query.or(`mandate_title.ilike.%${filters.search}%,mandate_reference.ilike.%${filters.search}%`)
  }
  
  const { data, error } = await query
    .order('created_date', { ascending: false })
  
  if (error) throw error
  return data || []
}

/**
 * Update simulated mandate
 */
export async function updateSimMandate(mandateId, updates) {
  const userId = await getCurrentUserId()
  
  // Only allow updates if user owns the mandate
  const mandate = await getSimMandateById(mandateId)
  if (mandate.user_id !== userId) {
    throw new Error('You can only edit your own mandates')
  }
  
  // Can't edit if approved (unless it's practice mode - we're lenient for learning)
  if (mandate.document_status === 'approved' && !mandate.is_practice_mode) {
    throw new Error('Approved mandates cannot be edited')
  }
  
  const { data, error } = await simDb
    .from('project_mandates')
    .update({
      ...updates,
      updated_at: new Date().toISOString(),
    })
    .eq('id', mandateId)
    .select()
    .single()
  
  if (error) throw error
  return data
}

/**
 * Delete simulated mandate (practice only)
 */
export async function deleteSimMandate(mandateId) {
  const userId = await getCurrentUserId()
  
  // Only allow deletion if user owns it
  const mandate = await getSimMandateById(mandateId)
  if (mandate.user_id !== userId) {
    throw new Error('You can only delete your own mandates')
  }
  
  const { data, error } = await simDb
    .from('project_mandates')
    .update({
      is_active: false,
      updated_at: new Date().toISOString(),
    })
    .eq('id', mandateId)
    .select()
    .single()
  
  if (error) throw error
  return data
}

/**
 * Submit simulated mandate for review (practice workflow)
 */
export async function submitSimMandateForReview(mandateId) {
  return updateSimMandate(mandateId, {
    document_status: 'submitted'
  })
}

/**
 * Approve simulated mandate (practice workflow)
 */
export async function approveSimMandate(mandateId) {
  return updateSimMandate(mandateId, {
    document_status: 'approved'
  })
}

