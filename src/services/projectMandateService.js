import { platformDb } from './supabase/supabaseClient'

/**
 * Get current user's internal ID from auth user ID
 */
async function getCurrentUserId() {
  const { data: { user: authUser }, error: authError } = await platformDb.auth.getUser()

  if (authError) throw new Error('Auth error: ' + authError.message)
  if (!authUser) throw new Error('User not authenticated')

  const { data: userData, error } = await platformDb
    .from('users')
    .select('id')
    .eq('auth_user_id', authUser.id)
    .maybeSingle()

  if (error) throw new Error('Database error: ' + error.message)

  // Fallback to auth user ID if not found in users table
  return userData?.id || authUser.id
}

/**
 * Create a new mandate (no project ID required initially)
 */
export async function createMandate(mandateData) {
  const userId = await getCurrentUserId()
  
  // Extract associated documents and stakeholders (stored as JSON arrays)
  const { associated_documents, stakeholders, ...mainMandateData } = mandateData
  
  const insertData = {
    ...mainMandateData,
    // project_id can be NULL (pre-project document)
    document_status: mainMandateData.document_status || 'draft',
    version_number: mainMandateData.version_number || '1.0',
    created_date: mainMandateData.created_date || new Date().toISOString().split('T')[0],
    is_standalone: mainMandateData.is_standalone !== undefined ? mainMandateData.is_standalone : true,
    quality_priority: mainMandateData.quality_priority || 'balanced',
    is_active: mainMandateData.is_active !== undefined ? mainMandateData.is_active : true,
    created_by: userId,
    updated_by: userId,
  }
  
  // mandate_reference will be auto-generated by trigger if not provided
  
  const { data, error } = await platformDb
    .from('project_mandates')
    .insert(insertData)
    .select()
    .single()
  
  if (error) throw error
  
  const mandateId = data.id
  
  // Process associated documents (Section 10)
  if (associated_documents) {
    try {
      const documents = typeof associated_documents === 'string' 
        ? JSON.parse(associated_documents || '[]')
        : associated_documents
      
      if (Array.isArray(documents) && documents.length > 0) {
        for (const doc of documents) {
          await addAssociatedDocument(mandateId, {
            document_type: doc.document_type || 'other',
            document_title: doc.document_title,
            document_url: doc.document_url || null,
            reference_number: doc.reference_number || null,
            display_order: documents.indexOf(doc)
          })
        }
      }
    } catch (err) {
      console.warn('Error adding associated documents:', err)
      // Don't fail mandate creation if documents fail
    }
  }
  
  // Process stakeholders (Section 12)
  if (stakeholders) {
    try {
      const stakeholderList = typeof stakeholders === 'string'
        ? JSON.parse(stakeholders || '[]')
        : stakeholders
      
      if (Array.isArray(stakeholderList) && stakeholderList.length > 0) {
        for (const stakeholder of stakeholderList) {
          await addStakeholder(mandateId, {
            stakeholder_type: stakeholder.stakeholder_type || 'customer',
            stakeholder_name: stakeholder.stakeholder_name,
            stakeholder_organisation: stakeholder.stakeholder_organisation || null,
            stakeholder_role: stakeholder.stakeholder_role || null,
            contact_email: stakeholder.contact_email || null,
            is_primary: stakeholder.is_primary || false,
            display_order: stakeholderList.indexOf(stakeholder)
          })
        }
      }
    } catch (err) {
      console.warn('Error adding stakeholders:', err)
      // Don't fail mandate creation if stakeholders fail
    }
  }
  
  // Return the created mandate with related data
  return getMandateById(mandateId)
}

/** UUID v4 pattern for detecting internal IDs vs mandate codes */
const UUID_REGEX = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i

/**
 * Single-query fetch for the mandate view page.
 * Fetches mandate + approvals + constraints + documents + stakeholders in ONE
 * HTTP round-trip instead of 5, cutting load time from seconds to milliseconds.
 */
export async function getMandateViewData(identifier) {
  if (!identifier || typeof identifier !== 'string') return null
  const trimmed = identifier.trim()
  if (!trimmed) return null

  let query = platformDb
    .from('project_mandates')
    .select(`
      *,
      project:project_id (id, project_name, project_code, status_id),
      programme:programme_id (id, programme_name, programme_code),
      proposed_executive:proposed_executive_id (id, email, full_name),
      proposed_pm:proposed_pm_id (id, email, full_name),
      creator:created_by (id, email, full_name),
      updater:updated_by (id, email, full_name),
      mandate_approvals (
        id, approval_status, approval_comments, approval_date, approval_at,
        approval_ip_address, approver_name, created_at
      ),
      mandate_constraints (
        id, operand, value_numeric, value_min, value_max, value_text, value_date,
        unit, notes, display_order, is_active,
        constraint_category:constraint_categories (
          id, code, name, description, value_type, supports_operands,
          unit_options, operand_options, dropdown_options
        )
      ),
      mandate_associated_documents (
        id, document_type, document_title, document_url, reference_number,
        display_order, is_deleted
      ),
      mandate_customers_users (
        id, stakeholder_type, stakeholder_name, stakeholder_organisation,
        stakeholder_role, contact_email, is_primary, display_order, is_deleted
      )
    `)
    .eq('is_deleted', false)

  if (UUID_REGEX.test(trimmed)) {
    query = query.eq('id', trimmed)
  } else {
    query = query.eq('mandate_reference', trimmed)
  }

  const { data, error } = await query.single()
  if (error) throw error
  return data
}

/**
 * Resolve a mandate identifier (UUID or mandate_reference) to full mandate.
 * Enables human-readable URLs like /pmo/mandates/MAN-2026-001/edit.
 */
export async function getMandateByIdOrReference(identifier) {
  if (!identifier || typeof identifier !== 'string') return null
  const trimmed = identifier.trim()
  if (!trimmed) return null
  if (UUID_REGEX.test(trimmed)) {
    return getMandateById(trimmed)
  }
  return getMandateByReference(trimmed)
}

/** Minimal columns needed for Create Project prefill — no joins, fast. */
const MANDATE_PREFILL_COLUMNS = 'id, mandate_reference, mandate_title, purpose, background, project_objectives, outline_business_case, proposed_executive_id, proposed_executive_name'

/**
 * Fetch only the mandate fields needed for project create prefill (no joins).
 * Use this when defaulting form fields so the request completes in milliseconds.
 */
export async function getMandatePrefill(identifier) {
  if (!identifier || typeof identifier !== 'string') return null
  const trimmed = identifier.trim()
  if (!trimmed) return null
  const isUuid = UUID_REGEX.test(trimmed)
  const { data, error } = await platformDb
    .from('project_mandates')
    .select(MANDATE_PREFILL_COLUMNS)
    .eq(isUuid ? 'id' : 'mandate_reference', trimmed)
    .eq('is_deleted', false)
    .single()
  if (error) {
    if (error.code === 'PGRST116') return null
    throw error
  }
  return data
}

