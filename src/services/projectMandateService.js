/**
 * Get all mandates with filtering
 */
export async function getAllMandates(organisationId = null, filters = {}) {
  let query = platformDb
    .from('project_mandates')
    .select(`
      *,
      project:project_id (id, project_name, project_code, status_id),
      programme:programme_id (id, programme_name, programme_code),
      proposed_executive:proposed_executive_id (id, email, full_name),
      proposed_pm:proposed_pm_id (id, email, full_name)
    `)
    .eq('is_deleted', false)
  
  if (filters.status) {
    query = query.eq('document_status', filters.status)
  }
  
  if (filters.is_standalone !== undefined) {
    query = query.eq('is_standalone', filters.is_standalone)
  }
  
  if (filters.programme_id) {
    query = query.eq('programme_id', filters.programme_id)
  }
  
  if (filters.project_id !== undefined) {
    if (filters.project_id === null) {
      query = query.is('project_id', null)
    } else {
      query = query.eq('project_id', filters.project_id)
    }
  }
  
  if (filters.search) {
    query = query.or(`mandate_title.ilike.%${filters.search}%,mandate_reference.ilike.%${filters.search}%,purpose.ilike.%${filters.search}%`)
  }
  
  const { data, error } = await query
    .order('created_date', { ascending: false })
  
  if (error) throw error
  return data || []
}

/**
 * Update an existing mandate
 * @param {string} mandateId - Mandate UUID
 * @param {object} updates - Mandate fields to update
 * @param {object} options - { skipEditCheck: true } when caller already verified editability (avoids redundant getMandateById)
 */
export async function updateMandate(mandateId, updates, options = {}) {
  // Run userId lookup and edit check in parallel (skip edit check when page already verified)
  const [userId, canEdit] = options.skipEditCheck
    ? [await getCurrentUserId(), true]
    : await Promise.all([
        getCurrentUserId(),
        canEditMandate(mandateId).catch(() => true)
      ])

  if (!canEdit) {
    throw new Error('Mandate cannot be edited (already approved or linked to project)')
  }

  // Extract fields stored in separate tables
  const { associated_documents, stakeholders, constraints, ...mainMandateData } = updates

  const updateData = {
    ...mainMandateData,
    updated_by: userId,
  }

  const { data: updatedMandate, error } = await platformDb
    .from('project_mandates')
    .update(updateData)
    .eq('id', mandateId)
    .select()
    .maybeSingle()

  if (error) throw error
  if (!updatedMandate) throw new Error('Mandate update did not affect any rows. Your changes may not have been saved. Check permissions or try again.')

  // Run associated_documents and stakeholders updates in parallel when both present
  const docPromise = associated_documents !== undefined && (async () => {
    try {
      await platformDb.from('mandate_associated_documents').delete().eq('mandate_id', mandateId)
      const documents = typeof associated_documents === 'string'
        ? JSON.parse(associated_documents || '[]')
        : associated_documents
      if (Array.isArray(documents) && documents.length > 0) {
        const docsToInsert = documents.map((doc, index) => ({
          mandate_id: mandateId,
          document_type: doc.document_type || 'other',
          document_title: doc.document_title,
          document_url: doc.document_url || null,
          reference_number: doc.reference_number || null,
          display_order: index,
          created_by: userId,
          updated_by: userId
        }))
        await platformDb.from('mandate_associated_documents').insert(docsToInsert)
      }
    } catch (err) {
      console.warn('Error updating associated documents:', err)
    }
  })()

  const stakeholderPromise = stakeholders !== undefined && (async () => {
    try {
      await platformDb.from('mandate_customers_users').delete().eq('mandate_id', mandateId)
      const stakeholderList = typeof stakeholders === 'string'
        ? JSON.parse(stakeholders || '[]')
        : stakeholders
      if (Array.isArray(stakeholderList) && stakeholderList.length > 0) {
        const stakeholdersToInsert = stakeholderList.map((stakeholder, index) => ({
          mandate_id: mandateId,
          stakeholder_type: stakeholder.stakeholder_type || 'customer',
          stakeholder_name: stakeholder.stakeholder_name,
          stakeholder_organisation: stakeholder.stakeholder_organisation || null,
          stakeholder_role: stakeholder.stakeholder_role || null,
          contact_email: stakeholder.contact_email || null,
          is_primary: stakeholder.is_primary || false,
          display_order: index,
          created_by: userId,
          updated_by: userId
        }))
        await platformDb.from('mandate_customers_users').insert(stakeholdersToInsert)
      }
    } catch (err) {
      console.warn('Error updating stakeholders:', err)
    }
  })()

  if (docPromise || stakeholderPromise) {
    await Promise.all([docPromise, stakeholderPromise].filter(Boolean))
  }

  return updatedMandate
}

/**
 * Delete a mandate (soft delete - only for drafts)
 */
export async function deleteMandate(mandateId) {
  const userId = await getCurrentUserId()
  
  // Only allow deletion of draft mandates
  const mandate = await getMandateById(mandateId)
  if (mandate.document_status !== 'draft') {
    throw new Error('Only draft mandates can be deleted')
  }
  
  if (mandate.project_id) {
    throw new Error('Cannot delete mandate linked to a project')
  }
  
  const { data, error } = await platformDb
    .from('project_mandates')
    .update({
      is_deleted: true,
      deleted_at: new Date().toISOString(),
      deleted_by: userId,
      updated_by: userId,
    })
    .eq('id', mandateId)
    .select()
    .single()
  
  if (error) throw error
  return data
}

